function DesenhaCubo(P)
    P = [ [0; 0; 0], [0; 1; 0],  [1; 1; 0], [1; 0; 0], [0; 0; 1], [0; 1; 1], [1; 1; 1], [1; 0; 1]];
    %P0 = [H*P(:,1), H*P(:,1), H*P(:,1), H*P(:,1), H*P(:,1), H*P(:,1), H*P(:,1), H*P(:,1)];
    %P = P0;
    
    figure(1)
    hold on
    axis equal;
    grid on;
    xlabel('X','FontSize',18);
    ylabel('Y','FontSize',18);
    zlabel('Z','FontSize',18);
    xlim([-2, 2]);
    ylim([-2, 2]);
    zlim([-2, 2]);
    
    
    %%
    %Face de Baixo
    plot3([P(1,1); P(1,2)],[P(2,1); P(2,2)],[P(3,1); P(3,2)], 'r')
    plot3([P(1,2); P(1,3)],[P(2,2); P(2,3)],[P(3,2); P(3,3)], 'k')
    plot3([P(1,3); P(1,4)],[P(2,3); P(2,4)],[P(3,3); P(3,4)], 'k')
    plot3([P(1,4); P(1,1)],[P(2,4); P(2,1)],[P(3,4); P(3,1)], 'b')
    %Face de Cima
    plot3([P(1,5); P(1,6)],[P(2,5); P(2,6)],[P(3,5); P(3,6)], 'k')
    plot3([P(1,6); P(1,7)],[P(2,6); P(2,7)],[P(3,6); P(3,7)], 'k')
    plot3([P(1,7); P(1,8)],[P(2,7); P(2,8)],[P(3,7); P(3,8)], 'k')
    plot3([P(1,8); P(1,5)],[P(2,8); P(2,5)],[P(3,8); P(3,5)], 'k')
    %Linhas Verticais
    plot3([P(1,1); P(1,5)],[P(2,1); P(2,5)],[P(3,1); P(3,5)], 'g')
    plot3([P(1,2); P(1,6)],[P(2,2); P(2,6)],[P(3,2); P(3,6)], 'k')
    plot3([P(1,3); P(1,7)],[P(2,3); P(2,7)],[P(3,3); P(3,7)], 'k')
    plot3([P(1,4); P(1,8)],[P(2,4); P(2,8)],[P(3,4); P(3,8)], 'k')
    
    %%
    %Sistema Fixo Mundo
    plot3([.5; 0],[0; 0],[0; 0], 'y')
    plot3([0; 0],[.5; 0],[0; 0], 'm')
    plot3([0; 0],[0; 0],[.5; 0], 'c')

end